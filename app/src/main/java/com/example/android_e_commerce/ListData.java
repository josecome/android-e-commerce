package com.example.android_e_commerce;

public class ListData {
    String nametype, name, description;
    double price;
    int image;
    public ListData(String nametype, String name, String description, double price, int image) {
        this.nametype = nametype;
        this.name = name;
        this.description = description;
        this.price = price;
        this.image = image;
    }
}
